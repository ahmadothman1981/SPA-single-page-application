<template>
    <form @submit.prevent="register">
        <div class="w-1/2 mx-auto">
            <div>
            <label for="name" class="label">Name</label>
            <input type="text" id="name" name="name" required class="input" v-model="form.name"/>
            <div class="input-error" v-if="form.errors.name">{{ form.errors.name }}</div>
            </div>
            <div class="mt-4">   
            <label for="email" class="label">Email</label>
            <input type="email" id="email" name="email" required class="input" v-model="form.email"/>
            <div class="input-error" v-if="form.errors.email">{{ form.errors.email }}</div>
            </div>
            <div class="mt-4">
            <label for="password" class="label">Password</label>
            <input type="password" id="password" name="password" required class="input" v-model="form.password"/>
            <div class="input-error" v-if="form.errors.password">{{ form.errors.password }}</div>   
            </div>
            <div class="mt-4">  
            <label for="password_confirmation" class="label">Password Confirmation</label>
            <input type="password" id="password_confirmation" name="password_confirmation" required class="input" v-model="form.password_confirmation"/>
            <div class="input-error" v-if="form.errors.password_confirmation">{{ form.errors.password_confirmation }}</div>   
            </div>
            <div class="mt-4">
            <button type="submit" class="btn-primary w-full ">Create Account</button>
            </div>
        </div>
    </form>
</template>
<script setup>
import {useForm } from '@inertiajs/vue3';

const form = useForm({
    name:'',
    email: '',
    password: '',
    password_confirmation:''
});
const register = () => form.post('/register');
</script>